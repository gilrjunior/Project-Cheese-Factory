    public TelaPedidoCliente(String cpf) {
        
        initComponents();
        
        cpf2 = cpf;
        
        try{

             fabricadao = new FabricaDAO();

        }catch (ClassNotFoundException ex){

            System.out.println("\n Erro ao conectar: "+ex.toString());

        } catch (SQLException ex) {
            Logger.getLogger(MostraCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        dtm_tabela = (DefaultTableModel) jTable1.getModel() ;
        dtm_tabela2 = (DefaultTableModel) jTable2.getModel() ;
        
        Pedido pedido = fabricadao.mostraPedido(cpf);
        ArrayList <QueijoPedido> listaQpedido = fabricadao.mostra_ID_queijos_pedido(pedido.getId_pedido());
        
        if(pedido != null){   
            dtm_tabela.addRow(new Object[]{pedido.getId_pedido(), pedido.getCpf(), pedido.getData(), pedido.getPrazo_entrega()});
        }
        
        for(QueijoPedido Qp: listaQpedido){
          dtm_tabela2.addRow(new Object[]{Qp.getId_queijo(), Qp.getQnt()});
        }
 
    }

